namespace MariBot.DiscordPatterns
{
    /// <summary>
    /// Specifies the permission value. 
    /// </summary>
    public enum MariDiscordPermValue
    {
        /// <summary> 
        /// Allows this permission. 
        /// </summary>
        Allow,

        /// <summary> 
        /// Denies this permission. 
        /// </summary>
        Deny,

        /// <summary> 
        /// Inherits the permission settings. 
        /// </summary>
        Inherit
    }
}